// Helper to get full car data
export const getCars = (): Car[] => {
  return trims.map(trim => {
    const model = models.find(m => m.id === trim.model_id)!;
    const brand = brands.find(b => b.id === model.brand_id)!;
    return {
      ...trim,
      model,
      brand,
    };
  });
};

export const getCarById = (id: string): Car | undefined => {
  const trim = trims.find(t => t.id === id);
  if (!trim) return undefined;
  
  const model = models.find(m => m.id === trim.model_id)!;
  const brand = brands.find(b => b.id === model.brand_id)!;
  
  return {
    ...trim,
    model,
    brand,
  };
};

